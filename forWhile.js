//arquivo for e while
let cont = 1
while(cont <= 10)
{
    console.log(`valor do contador: ${cont}`)
    cont++
}

//for
for(let n = 1; n<=10; n++)
{
console.log(`valor do contador: ${n}`)
}


const notas = [8, 9, 5, 3, 10, 4]
for(let m=0; m < notas.length; m++){
    console.log(`Nota (${m+1})= ${notas[m]}`)
}